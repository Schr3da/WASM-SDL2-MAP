use sdl2::pixels::Color;

pub fn get_light_green() -> Color {
    Color::RGBA(205, 234, 225, 255)
}

pub fn get_green() -> Color {
    Color::RGBA(157, 232, 209, 255)
}

pub fn get_dark_green() -> Color {
    Color::RGBA(25, 225, 165, 255)
}

pub fn get_light_blue() -> Color {
    Color::RGBA(108, 194, 255, 255)
}

pub fn get_blue() -> Color {
    Color::RGBA(42, 167, 255, 255)
}


pub fn get_dark_blue() -> Color {
    Color::RGBA(21, 81, 123, 255)
}

pub fn get_clear_color() -> Color {
    Color::RGB(0, 0, 0)
}
